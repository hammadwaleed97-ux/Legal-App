# --- أولاً: الإدارة العامة للقضايا ---
with st.expander("⚖️ أولاً: الإدارة العامة للقضايا (القسم القضائى)", expanded=False):
    tab_court = st.tabs(["القضاء العادى", "مجلس الدولة", "تسجيل الدعاوى والطعون", "البحث والسوابق"])
    
    with tab_court[0]: # القضاء العادي
        court_level = st.radio("المحكمة:", ["الابتدائية", "الاستئنافية", "النقض"], horizontal=True)
        
        if court_level == "الابتدائية":
            mode = st.selectbox("نوع الصياغة:", ["مذكرة دفاع (الهيئة مدعى عليها)", "مذكرة دفاع (الهيئة مدعية)"])
