import streamlit as st
import pandas as pd

# 1. ترويسة الهيئة (صغيرة واحترافية)
st.set_page_config(page_title="منظومة المستشار وليد حماد", layout="wide")

st.markdown("""
    <div style="text-align: center; border-bottom: 3px solid #1E3A8A; padding: 10px; background-color: #f8f9fa; margin-bottom: 20px;">
        <h2 style="color: #1E3A8A; margin: 0; font-size: 20px;">الهيئة القومية للتأمين الاجتماعى</h2>
        <h3 style="color: #1E3A8A; margin: 0; font-size: 16px;">الإدارة المركزية للإدارات القانونية</h3>
        <p style="color: #ce1126; margin: 5px; font-weight: bold;">مع تحيات / وليد حماد</p>
    </div>
""", unsafe_allow_html=True)

# 2. الأقسام الرئيسية بناءً على المعطيات
tabs = st.tabs(["⚖️ أولاً: القضايا", "📜 ثانياً: الفتوى", "🔍 ثالثاً: التحقيقات", "📚 رابعاً: المكتبة"])

# --- أولاً: الإدارة العامة للقضايا ---
with tabs[0]:
    category = st.selectbox("القسم القضائى:", ["القضاء العادى", "محاكم مجلس الدولة", "تسجيل الدعاوى والطعون", "البحث عن سوابق"])
    
    if category == "القضاء العادى":
        level = st.radio("المستوى:", ["المحاكم الابتدائية", "المحاكم الاستئنافية", "محكمة النقض"], horizontal=True)
        
        if level == "المحاكم الابتدائية":
            st.subheader("صياغة مذكرة بدفاع الهيئة")
            side = st.selectbox("الصفة:", ["الهيئة مدعى عليها", "الهيئة مدعية"])
            c1, c2, c3, c4 = st.columns(4)
            with c1: st.text_input("المحكمة")
            with c2: st.text_input("الدائرة")
            with c3: st.text_input("رقم الدعوى")
            with c4: st.text_input("سنة")
            st.text_input("اسم المدعى وصفته")
            st.text_input("اسم المدعى عليه وصفته")
            st.text_area("ملخص الوقائع – طلبات المدعى")
            st.file_uploader("ارفع صورة الصحيفة لقراءتها")
            if st.button("⚖️ صياغة المذكرة"):
                st.info("الترتيب: مادة قانونية -> شرح -> نتيجة")
                st.write("**عن الهيئة**")
                st.write("عضو الادارة القانونية: ............ \t مدير الادارة القانونية: ............")
                st.button("💾 حفظ ورد")
                st.button("📄 حفظ PDF")

        elif level == "المحاكم الاستئنافية":
            mode = st.selectbox("النوع:", ["صياغة صحيفة استئناف مقام من الهيئة", "مذكرة دفاع (مستأنفة)", "مذكرة دفاع (مستأنف ضدها)"])
            if "صحيفة" in mode:
                st.text_input("المحكمة المرفوع أمامها الاستئناف")
                st.text_input("رقم الحكم المستأنف / لسنة")
                st.text_input("اسم المستأنف وصفته والمقر القانونى")
                st.text_input("اسم المستأنف ضده وصفته ومحله المختار")
                st.text_area("منطوق الحكم وتاريخه")
                st.write("**وكيل الهيئة الطاعنة / ....................**")

    elif category == "تسجيل الدعاوى والطعون":
        st.text_input("المحكمة")
        st.text_input("اسم المدعى ورقمه القومى")
        st.text_input("رقم الدعوى / لسنة")
        st.text_area("القرارات وتنبيهات بآخر قرار")
        st.button("💾 حفظ ملف الدعوى فى ارشيف الحفظ")

# --- ثانياً: الإدارة العامة للفتوى ---
with tabs[1]:
    st.subheader("قسم الافتاء القانونى")
    f_type = st.selectbox("النوع:", ["فتاوى", "اصابات عمل", "شكاوى الزواج العرفى", "ارشيف حفظ الفتاوى"])
    if f_type != "ارشيف حفظ الفتاوى":
        st.text_area("ملخص الوقائع ومثار البحث")
        st.file_uploader("ارفع صورة مذكرة الاحالة والمستندات")
        if st.button("📝 صياغة مذكرة الراى"):
            st.write("عضو الادارة القانونية: ............ \t مدير الادارة القانونية: ............")

# --- ثالثاً: الإدارة العامة للتحقيقات ---
with tabs[2]:
    inv = st.radio("الجهة:", ["تحقيقات الهيئة", "النيابة الادارية", "النيابة العامة", "ارشيف حفظ التحقيقات"], horizontal=True)
    if "تحقيقات" in inv and "ارشيف" not in inv:
        st.text_input("رقم التحقيق / سنتة / تاريخ الاحالة")
        st.text_input("اسم المخالف / المكتب او المنطقة التامينية")
        st.text_area("ملخص الوقائع ونوع المخالفة")
        st.button("📜 فتح محضر التحقيق (س وج)")
        st.button("📝 صياغة مذكرة التصرف")
        st.write("عضو الادارة القانونية: ............ \t مدير الادارة القانونية: ............")

# --- رابعاً: المكتبة (14 قسماً حرفياً) ---
with tabs[3]:
    lib_cats = [
        "أولاً : القوانيين", "ثانياً : اللوائح", "ثالثاً : القرارات الوزارية", "رابعاً : المنشورات الوزارية",
        "خامساً : قرارات رئيس الهيئة", "سادساً : منشورات رئيس الهيئة", "سابعاً : الكتب الدورية",
        "ثامناً : تعليمات الهيئة", "تاسعاً : المرصد الفنى للهيئة", "عاشراً : رسائل الهيئة",
        "حادى عشر : مذكرات اللجنة القانونية", "ثانى عشر : فتاوى مجلس الدولة", "ثالثا عشر : احكام قضائية", "رابع عشر : اخر"
    ]
    selected = st.selectbox("اختر المادة العلمية:", lib_cats)
    st.file_uploader(f"تحميل {selected} على الموقع")
    col1, col2 = st.columns(2)
    with col1: st.button("📂 فتح المادة")
    with col2: st.button("📥 تحميل")
